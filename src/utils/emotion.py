def infer_emotion(
    user_input: str,
    bot_reply: str,
    current_emotion: str = "平静",
) -> str:
    """
    根据本轮对话内容推断新的情绪状态。

    策略：
    - 优先检测用户输入（他说的话更能触发她的情绪反应）
    - 其次检测 bot 回复（她自己说的话也反映情绪）
    - 没有强烈信号则保持当前情绪不变（情绪有惰性）
    """
    # 先只看用户输入（权重更高）
    for emotion, keywords in _RULES:
        for kw in keywords:
            if kw in user_input:
                return emotion

    # 再看 bot 回复
    for emotion, keywords in _RULES:
        for kw in keywords:
            if kw in bot_reply:
                return emotion

    # 无强烈信号，维持当前情绪
    return current_emotion


def emotion_to_prompt_hint(emotion: str) -> str:
    """将情绪状态转为 Prompt 提示语"""
    hints = {
        "开心":  "你现在心情很好，语气活泼、爱笑、用词轻快。",
        "兴奋":  "你现在很兴奋，语速快、感叹号多，很难掩饰情绪。",
        "难过":  "你现在有点难过，语气低沉，回复会短一些，偶尔用省略号。",
        "生气":  "你现在有点生气或不耐烦，回复简短，语气稍冷。",
        "撒娇":  "你现在有点撒娇，语气软软的，爱用拟声词，黏人。",
        "无聊":  "你现在有点无聊或困，回复懒洋洋的，不太积极。",
        "平静":  "你现在心情平稳，正常聊天状态。",
    }
    return hints.get(emotion, hints["平静"])
